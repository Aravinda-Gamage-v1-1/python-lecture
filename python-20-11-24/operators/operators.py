#Arithmetic operators
y = 7 / 2
print(y) #3.5
y = 9 // 4
print(y) #2

y = 7 % 3
print(y) #1

#Assign operators
x = 5
x += 5
print(x) #10
x *= 5
print(x) #50

#Comparison operators
x = 5
y = 6
print(x == y) #False
print(x != y) #True

#Logical operators
print(x < 10 and y > 8)
print(x < 10 or y > 8)
print(not y > 8)
